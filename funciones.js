// Función que suma dos números

function sumar(a, b) { //Aquí se declara la función con ambos parámetros a y b (los dos números)
  return a + b; // Aquí se devuelve el argumento como la suma de ambos
}

// Función que multiplica dos números

function multiplicar(a, b) { //Aquí se declara la función con ambos parámetros a y b (los dos números)
  return a * b; // Aquí se devuelve el argumento como el producto de ambos
}
